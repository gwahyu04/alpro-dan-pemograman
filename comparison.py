
angka1 = float(input("Masukkan angka pertama: "))
angka2 = float(input("Masukkan angka kedua: "))

print(f"{angka1} == {angka2}: {angka1 == angka2}")  # Sama dengan
print(f"{angka1} != {angka2}: {angka1 != angka2}")  # Tidak sama dengan
print(f"{angka1} > {angka2}: {angka1 > angka2}")    # Lebih besar dari
print(f"{angka1} < {angka2}: {angka1 < angka2}")    # Lebih kecil dari
print(f"{angka1} >= {angka2}: {angka1 >= angka2}")  # Lebih besar atau sama dengan
print(f"{angka1} <= {angka2}: {angka1 <= angka2}")  # Lebih kecil atau sama dengan
